// Orientação a objetos é uma abordagem de programação que organiza o
// código de forma a representar objetos do mundo real, como pessoas,
// carros e animais, de uma maneira mais fácil de entender e manipular.
// Imagine que você tem uma classe chamada "Carro" que define como
// um carro deve se comportar, com métodos para ligar o motor, acelerar e
// frear, e propriedades como cor, marca e modelo. Quando você cria um
// objeto a partir dessa classe, você está criando uma instância única
// desse carro, com suas próprias características. Assim, a orientação a
// objetos permite criar programas mais organizados, onde cada objeto
// tem seu papel definido e pode interagir com outros objetos de forma
// controlada, facilitando a construção de sistemas complexos de maneira
// mais intuitiva.

